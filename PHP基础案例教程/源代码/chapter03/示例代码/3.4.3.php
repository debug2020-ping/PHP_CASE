<?php

// (1) Unix时间戳
echo time();                        // 输出结果(仅供参考)：1487666317
echo mktime(0, 0, 0, 2, 21, 2017);  // 输出结果：1487606400
echo strtotime('2017-2-21');        // 输出结果：1487606400
echo microtime();                   // 输出结果(仅供参考)：0.04142600 1487666098
echo microtime(true);               // 输出结果(仅供参考)：1487666098.0414

// (2) 格式化时间戳
echo date('Y-m-d H:i:s');           // 输出结果(仅供参考)：2017-02-21 16:48:16
echo date('Y-m-d', 1487666317);     // 输出结果：2017-02-21
